def get_lazy_phrase(days_inactive: int) -> str:
    if days_inactive >= 3:
        return random.choice(VERY_LAZY_PHRASES)
    return random.choice(LAZY_PHRASES)


def get_praise_phrase() -> str:
    return random.choice(PRAISE_PHRASES)
